// ============================================================================
// Helper to generate tribe weapons. Pattern: 5 units × 3 tiers per tribe.
// ============================================================================
type WeaponUnit = {
  unitName: string;
  t1Atk: number;
  t2Atk: number;
  t3Atk: number;
  t1Name: string;
  t2Name: string;
  t3Name: string;
};

function generateTribeWeapons(
  tribe: HeroItem['tribe'],
  startId: number,
  units: WeaponUnit[],
): HeroItem[] {
  const result: HeroItem[] = [];
  const strengthByTier = [250, 1000, 4000] as const;
  const rarityByTier: HeroItem['rarity'][] = ['common', 'uncommon', 'rare'];
  const priceByTier = [100, 500, 2000];

  for (let unitIdx = 0; unitIdx < units.length; unitIdx++) {
    const unit = units[unitIdx];
    const tiers = [
      { name: unit.t1Name, atk: unit.t1Atk },
      { name: unit.t2Name, atk: unit.t2Atk },
      { name: unit.t3Name, atk: unit.t3Atk },
    ];
    for (let tierIdx = 0; tierIdx < 3; tierIdx++) {
      const tier = tiers[tierIdx];
      const idx = unitIdx * 3 + tierIdx;
      result.push({
        id: startId + idx,
        imageId: startId + idx,
        name: tier.name.toUpperCase().replace(/\s+/g, '_').replace(/'/g, ''),
        displayName: tier.name,
        description: `+${strengthByTier[tierIdx]} strength; +${tier.atk} atk/def per ${unit.unitName}`,
        slot: 'right-hand',
        tier: (tierIdx + 1) as HeroItem['tier'],
        rarity: rarityByTier[tierIdx],
        category: 'right-hand',
        tribe,
        basePrice: priceByTier[tierIdx],
        heroBonus: [
          { attribute: 'power', value: strengthByTier[tierIdx] },
          { attribute: 'unitAttack', value: tier.atk, unit: unit.unitName },
          { attribute: 'unitDefence', value: tier.atk, unit: unit.unitName },
        ],
      });
    }
  }
  return result;
}

